package struct;

public class UserOutletJsonData {
	
	String outletName;
	long outletID;
	long userNumber;
	long totalComment;
	long commentFromAnonymous;
	
	public String getOutletName() {
		return outletName;
	}
	public void setOutletName(String outletName) {
		this.outletName = outletName;
	}
	public long getOutletID() {
		return outletID;
	}
	public void setOutletID(long outletID) {
		this.outletID = outletID;
	}
	public long getUserNumber() {
		return userNumber;
	}
	public void setUserNumber(long userNumber) {
		this.userNumber = userNumber;
	}
	public long getTotalComment() {
		return totalComment;
	}
	public void setTotalComment(long totalComment) {
		this.totalComment = totalComment;
	}
	public long getCommentFromAnonymous() {
		return commentFromAnonymous;
	}
	public void setCommentFromAnonymous(long commentFromAnonymous) {
		this.commentFromAnonymous = commentFromAnonymous;
	}
	
	

}
